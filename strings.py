# String Programs With All Function and Methods
print('******** WELCOME TO THE STRING WORLD ********') 
a='deepesh'
print(a)
print(a+' '+ 'soni')   # Concatenating Strings using '+' Operator
print(a*3)             # Printing Multiple String using '*' Operator
print(len(a))          # Printing length of String using len()
print(a.upper())
print(a[1:7:2])
# String Support Negative Index
a='deepesh'
print(a[-1],a[-2])
print(a[-5:-1])

# Loop To Print String
s=input('Enter The String Here :')
for i in s :
    print(i)

# Print Reverse String 
print(s[:])
print(s[::-1])

